#abs() returns the absolute value
print(abs(-4))

#sum() returns the sum of numbers and begins at the start value, the default being 0
print(sum([1,2,4]))
# print(sum(1,2,4,5)) #will throw an error, numbers have to be in a list or tuple
print(sum([1,2,4],7))
print(sum([1,2,4],-7))
print(sum([1,2,4],100))

#round rounds the number. (number, to how many decimals)
print(round(8.518375))
print(round(8.518375,2))
print(round(8.518375, None))